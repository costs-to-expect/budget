                    <h2>Helps to ensure bills are paid on time</h2>

                    <p>
                        Having all of your bills included in a budget helps to ensure that you’ll pay them on time.  Life can be busy
                        but incurring late fees can be expensive! A budget shows you an overview of everything which needs to be paid
                        and when so you shouldn’t fall behind with the bills.
                    </p>

                    <h2>Budgeting can help you to manage debt</h2>

                    <p>
                        If you’re already in debt, having a budget is a really important tool for managing it. Your budget can help you
                        to identify income that can go towards paying off debt, once all your bills have been paid. If you stick to
                        your budget, it’s unlikely that you’ll accumulate any further debt.
                    </p>

                    <h2>Having a budget can help build an emergency fund</h2>

                    <p>
                        It’s recommended that everyone should have an emergency fund of at least three months money to cover living
                        expenses. This gives you a bit of breathing space if unexpected expenses crop up or should the worse happen
                        and you lose your income for a period.
                    </p>

                    <h2>Helps to prevent impulse buying and overspending</h2>

                    <p>
                        If you struggle with impulse buying and keeping your spending under control, having a budget can help you
                        prevent overspending. A budget is just a plan for what you’ll do with your money so it doesn’t restrict you
                        from buying what you want. Using a budget just helps you to plan your spending so that you don’t spend more
                        than you have!
                    </p>

                    <h2>Budgeting helps achieve long-term financial goals</h2>

                    <p>
                        Having a plan for your money is important if you have long-term financial goals.  If you’ve got plans such as
                        buying a house or perhaps you want to start budgeting for a wedding,
                        having an overall budget will help to keep your finances on track and reach your goals quickly.
                    </p>

                    <h2>Building up your savings</h2>

                    <p>
                        You might think we’re repeating ourselves but savings is not the same as an emergency fund! The two should be
                        kept very separately. You might decide to save for something specific or simply save for a rainy day. Whatever
                        the reason for saving, budgeting makes you more accountable for your spending, hence making it more likely
                        that you’ll save money.
                    </p>

                    <h2>Budgeting can highlight bad habits</h2>

                    <p>
                        Remember what we said about accountability?! If you have an effective budget that includes all of your spending,
                        it can help you to identify bad spending habits which may be holding you back.  Perhaps you’re spending money on
                        things you really don’t need such as unused subscriptions. Your budget can help you shine a light on unnecessary
                        spending and redirect that money towards your financial goals.
                    </p>

                    <h2>Having a budget helps reduce stress</h2>

                    <p>
                        Living pay check to pay check and/or wondering how you’re going to pay the bills can be really stressful and
                        damaging to your mental health. Budgeting will give you and your finances structure so that you know what to
                        expect and when. If you can eliminate nasty financial surprises, this will help to reduce stress.
                    </p>

